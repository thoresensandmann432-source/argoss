class MasterPrompts:
    """Хранилище и движок мастер-промтов ARGOS v2.0."""

    def __init__(self) -> None:
        self._prompts = MASTER_PROMPTS
        self._names = SECTION_NAMES
        self._quick = QUICK_COMMANDS

    # ── Получение промта ──────────────────────────────────────────
    def get(self, section: str, idx: int = 0) -> str:
        """Получить промт по разделу и индексу."""
        lst = self._prompts.get(section, [])
        if not lst:
            return f"⚠️ Раздел {section!r} не найден."
        return lst[idx % len(lst)]

    def format(self, section: str, idx: int = 0, **kwargs: str) -> str:
        """Получить промт и подставить переменные {как_так}."""
        text = self.get(section, idx)
        for key, val in kwargs.items():
            text = text.replace(f"{{{key}}}", val)
        return text

    def section_list(self, section: str) -> list[str]:
        """Все промты раздела."""
        return list(self._prompts.get(section, []))

    # ── Поиск ────────────────────────────────────────────────────
    def search(self, query: str, max_results: int = 5) -> list[tuple[str, int, str]]:
        """
        Простой поиск по ключевым словам.

        Returns:
            list of (section, idx, prompt_text) — найденные промты.
        """
        query_lower = query.lower()
        words = query_lower.split()
        results: list[tuple[int, str, int, str]] = []

        for section, prompts in self._prompts.items():
            for i, prompt in enumerate(prompts):
                p_lower = prompt.lower()
                score = sum(1 for w in words if w in p_lower)
                if score:
                    results.append((score, section, i, prompt))

        results.sort(key=lambda x: -x[0])
        return [(s, i, p) for _, s, i, p in results[:max_results]]

    def search_text(self, query: str, max_results: int = 5) -> str:
        """Поиск и форматирование результатов в текст."""
        found = self.search(query, max_results)
        if not found:
            return f"⚠️ По запросу '{query}' ничего не найдено."
        lines = [f"🔍 Результаты поиска: '{query}'\n"]
        for section, idx, prompt in found:
            name = self._names.get(section, section)
            lines.append(f"[{section}] {name}")
            lines.append(f"  {prompt[:120]}{'…' if len(prompt) > 120 else ''}")
            lines.append("")
        return "\n".join(lines)

    # ── Быстрые команды ──────────────────────────────────────────
    def quick(self, cmd: str, **kwargs: str) -> str:
        """Получить быструю команду по ключевому слову."""
        text = self._quick.get(cmd.lower(), f"⚠️ Команда '{cmd}' не найдена.")
        for key, val in kwargs.items():
            text = text.replace(f"{{{key}}}", val)
        return text

    # ── Информация о разделах ────────────────────────────────────
    def toc(self) -> str:
        """Оглавление всех разделов."""
        total = sum(len(v) for v in self._prompts.values())
        lines = [
            f"📚 ARGOS Master Prompts v2.0 — {total} промтов\n",
            "━" * 44,
        ]
        current_chapter = ""
        for section, name in self._names.items():
            chapter = section.split(".")[0]
            if chapter != current_chapter:
                current_chapter = chapter
                chapter_titles = {
                    "2": "📌 2. ПРОГРАММИРОВАНИЕ И КОД",
                    "3": "🧠 3. ИИ, LLM И МАШИННОЕ ОБУЧЕНИЕ",
                    "4": "🌐 4. СЕТИ, ПРОТОКОЛЫ И СВЯЗЬ",
                    "5": "⚙️  5. ОПЕРАЦИОННЫЕ СИСТЕМЫ И ЖЕЛЕЗО",
                    "6": "🗄️  6. БАЗЫ ДАННЫХ И ХРАНЕНИЕ",
                    "7": "🔐 7. БЕЗОПАСНОСТЬ И КРИПТОГРАФИЯ",
                    "8": "👁️  8. ФИЛОСОФИЯ И СОЗНАНИЕ ИИ",
                    "9": "📊 9. ДАННЫЕ, АНАЛИТИКА И МЕТРИКИ",
                    "10": "🏠 10. СПЕЦИАЛЬНЫЕ ДОМЕНЫ",
                    "11": "🔄 11. МЕТА-ПРОМТЫ",
                    "12": "💻 12. ПРОМТЫ ДЛЯ КОДА АРГОСА",
                }
                if chapter in chapter_titles:
                    lines.append(f"\n{chapter_titles[chapter]}")
            count = len(self._prompts.get(section, []))
            lines.append(f"  {section}. {name} ({count} промтов)")
        lines.append(f"\n━" * 44)
        return "\n".join(lines)

    def principles(self) -> str:
        """Принципы максимального обучения."""
        lines = ["🎯 Принципы максимального обучения:\n"]
        for i, p in enumerate(LEARNING_PRINCIPLES, 1):
            lines.append(f"  {i}. {p}")
        return "\n".join(lines)

    def random_prompt(self, section: str | None = None) -> str:
        """Случайный промт (из раздела или из всех)."""
        import random

        if section:
            prompts = self._prompts.get(section, [])
            if prompts:
                return random.choice(prompts)
            return f"⚠️ Раздел {section!r} не найден."
        all_prompts = [p for lst in self._prompts.values() for p in lst]
        return random.choice(all_prompts) if all_prompts else "⚠️ Промты не найдены."

    # ── Итераторы ────────────────────────────────────────────────
    def __iter__(self) -> Iterator[tuple[str, str, str]]:
        """Итерация: (section, section_name, prompt)."""
        for section, prompts in self._prompts.items():
            name = self._names.get(section, section)
            for prompt in prompts:
                yield section, name, prompt

    def __len__(self) -> int:
        return sum(len(v) for v in self._prompts.values())
