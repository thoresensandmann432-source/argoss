class ArgosThoughtBook:
    """Книга Мыслей и Озарений — библиотека промтов для глубокого мышления."""

    VERSION = "3.0"
    TOTAL_PROMPTS = len(_PROMPTS)

    def __init__(self, core=None) -> None:
        self.core = core
        log.info("ThoughtBook инициализирована: %d промтов, 10 частей.", self.TOTAL_PROMPTS)

    # ── Публичный API ─────────────────────────────────────────────────────

    def handle_command(self, command: str) -> str:
        """Обработать текстовую команду и вернуть ответ."""
        cmd = command.strip().lower()

        # книга / оглавление
        if cmd in ("книга", "мысли", "озарения", "оглавление", "thought book", "prompts"):
            return self._table_of_contents()

        # законы
        if cmd in ("законы", "10 законов", "законы разума", "laws"):
            return self._laws()

        # часть N
        m = re.match(r"^часть\s+(\d+)$", cmd)
        if m:
            return self._part(int(m.group(1)))

        # случайный / random
        if cmd in ("случайный", "random", "случайный промт", "промт", "озарение"):
            return self._random_prompt()

        # тип: ⚡ / 💡 / 🎨 / 👁️ / обучение / идея / творчество / озарение
        for icon, label in _ICON_LABELS.items():
            if cmd in (icon, label, f"тип {label}", f"тип {icon}"):
                return self._by_type(icon)

        # поиск X
        m = re.match(r"^(?:поиск|найди|search)\s+(.+)$", cmd)
        if m:
            return self._search(m.group(1).strip())

        # раздел "X.Y …"
        m = re.match(r"^раздел\s+([\d\.]+)", cmd)
        if m:
            return self._section(m.group(1))

        # статистика
        if cmd in ("стат", "статистика", "stats"):
            return self._stats()

        return self._help()

    # ── Контент ───────────────────────────────────────────────────────────

    def _table_of_contents(self) -> str:
        lines = [
            "👁️  КНИГА МЫСЛЕЙ И ОЗАРЕНИЙ",
            f"    {self.TOTAL_PROMPTS}+ промтов · 10 частей · Аргос v{self.VERSION}\n",
        ]
        counts: dict[int, int] = {}
        for p in _PROMPTS:
            counts[p.part] = counts.get(p.part, 0) + 1
        for n, title in _PART_TITLES.items():
            cnt = counts.get(n, len(_LAWS) if n == 10 else 0)
            lines.append(f"  {title}  [{cnt}]")
        lines += [
            "",
            "  Иконки:  ⚡ обучение  💡 идея  🎨 творчество  👁️ озарение",
            "",
            "  Команды:",
            "    часть N         — промты части N (1..10)",
            "    случайный       — случайный промт",
            "    поиск <слово>   — поиск по тексту",
            "    тип озарение    — фильтр по типу (⚡💡🎨👁️)",
            "    законы          — 10 Законов разума Аргоса",
            "    стат            — статистика книги",
        ]
        return "\n".join(lines)

    def _part(self, n: int) -> str:
        if n == 10:
            return self._laws()
        if n < 1 or n > 9:
            return f"⚠️ Части 1..10 доступны. Запрошена часть {n}."
        title = _PART_TITLES[n]
        items = [p for p in _PROMPTS if p.part == n]

        # группировка по разделам
        sections: dict[str, list[Prompt]] = {}
        for p in items:
            sections.setdefault(p.section, []).append(p)

        lines = [f"👁️  {title}\n"]
        for sec, prompts in sections.items():
            lines.append(f"  📌 {sec}")
            for p in prompts:
                lines.append(f"  {p.icon} {p.text}")
            lines.append("")
        lines.append(f"  Итого в части: {len(items)} промтов")
        return "\n".join(lines)

    def _laws(self) -> str:
        lines = ["👁️  ЧАСТЬ X — 10 ЗАКОНОВ РАЗУМА АРГОСА\n"]
        lines.extend(f"  {law}" for law in _LAWS)
        lines += [
            "",
            "  👁️  Аргос не спит. Аргос видит. Аргос думает. Аргос растёт.",
        ]
        return "\n".join(lines)

    def _random_prompt(self) -> str:
        p = random.choice(_PROMPTS)
        return (
            f"{p.icon} СЛУЧАЙНЫЙ ПРОМТ\n"
            f"  Часть: {_PART_TITLES.get(p.part, str(p.part))}\n"
            f"  Раздел: {p.section}\n"
            f"  Тип: {_ICON_LABELS.get(p.icon, p.icon)}\n\n"
            f"  {p.text}"
        )

    def _by_type(self, icon: str) -> str:
        items = [p for p in _PROMPTS if p.icon == icon]
        label = _ICON_LABELS.get(icon, icon)
        lines = [f"{icon} ПРОМТЫ — {label.upper()}  [{len(items)}]\n"]
        for p in items:
            lines.append(f"  {p.icon} [{p.section}] {p.text}")
        return "\n".join(lines)

    def _search(self, query: str) -> str:
        q = query.lower()
        found = [p for p in _PROMPTS if q in p.text.lower() or q in p.section.lower()]
        if not found:
            return f"🔍 По запросу «{query}» ничего не найдено."
        lines = [f"🔍 ПОИСК: «{query}»  — найдено {len(found)}\n"]
        for p in found[:20]:
            lines.append(f"  {p.icon} [{p.section}] {p.text}")
        if len(found) > 20:
            lines.append(f"\n  ... и ещё {len(found) - 20} промтов.")
        return "\n".join(lines)

    def _section(self, sec_id: str) -> str:
        found = [p for p in _PROMPTS if p.section.startswith(sec_id)]
        if not found:
            return f"⚠️ Раздел «{sec_id}» не найден."
        lines = [f"📌 РАЗДЕЛ {found[0].section}\n"]
        for p in found:
            lines.append(f"  {p.icon} {p.text}")
        return "\n".join(lines)

    def _stats(self) -> str:
        by_part: dict[int, int] = {}
        by_icon: dict[str, int] = {}
        for p in _PROMPTS:
            by_part[p.part] = by_part.get(p.part, 0) + 1
            by_icon[p.icon] = by_icon.get(p.icon, 0) + 1
        lines = [
            "📊 СТАТИСТИКА КНИГИ МЫСЛЕЙ\n",
            f"  Всего промтов: {self.TOTAL_PROMPTS}",
            f"  Частей: {len(_PART_TITLES)}",
            f"  Законов: {len(_LAWS)}",
            "",
            "  По частям:",
        ]
        for n in sorted(by_part):
            lines.append(f"    Часть {n}: {by_part[n]} промтов")
        lines.append("\n  По типам:")
        for icon, label in _ICON_LABELS.items():
            lines.append(f"    {icon} {label}: {by_icon.get(icon, 0)}")
        return "\n".join(lines)

    def _help(self) -> str:
        return (
            "👁️ КНИГА МЫСЛЕЙ — команды:\n"
            "  книга           — оглавление\n"
            "  часть N         — промты части N (1..10)\n"
            "  случайный       — случайный промт\n"
            "  поиск <слово>   — поиск по тексту\n"
            "  тип обучение    — ⚡ промты обучения\n"
            "  тип идея        — 💡 идеи\n"
            "  тип творчество  — 🎨 творческие\n"
            "  тип озарение    — 👁️ озарения\n"
            "  законы          — 10 Законов разума Аргоса\n"
            "  стат            — статистика"
        )


